	public List<Director> getVertici(int anno, Map<Integer, Director> idMap) {
		
		String sql = "SELECT distinct md.director_id "
				+ "FROM movies m, movies_directors md "
				+ "WHERE m.id = md.movie_id AND m.year = ?";
		List<Director> result = new ArrayList<Director>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setInt(1, anno);
			ResultSet res = st.executeQuery();
			while (res.next()) {
				
				result.add(idMap.get(res.getInt("director_id")));
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	public Set<Integer> getActorsYearDirectSpec(int anno, int director) {
		
		String sql = "SELECT DISTINCT r.actor_id "
				+ "FROM movies m, movies_directors md, roles r "
				+ "WHERE m.id = md.movie_id AND m.year = ? AND r.movie_id = m.id AND md.director_id = ?";
		
		Set<Integer> result = new HashSet<Integer>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setInt(1, anno);
			st.setInt(2, director);
			ResultSet res = st.executeQuery();
			while (res.next()) {
				
				result.add(res.getInt("actor_id"));
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	


	
	
	
	
	
	
